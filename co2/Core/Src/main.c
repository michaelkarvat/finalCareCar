/* Includes ------------------------------------------------------------------*/
#include "main.h"
#include <stdio.h>

/* Private variables ---------------------------------------------------------*/
UART_HandleTypeDef huart1; // UART for sensor
UART_HandleTypeDef huart2; // UART for debugging (printf redirection)

/* USER CODE BEGIN PV */
// Buffers for UART communication
uint8_t txBuffer[9] = {0xFF, 0x01, 0x86, 0x00, 0x00, 0x00, 0x00, 0x00, 0x79}; // Command to read CO2
uint8_t rxBuffer[9]; // Buffer to hold the sensor's response
/* USER CODE END PV */

/* Function prototypes -------------------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USART1_UART_Init(void);
